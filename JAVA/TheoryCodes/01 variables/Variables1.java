class Variables1
{
	public static void main(String[] args) 
	{
		//this is declaration;
		String name;
		byte age;
		float sallary;
		long phno;
		short id;
		int pinno;

		//this is initialization (initialization = storing value in variables) to variables;
		name = "Max";
		age = 25;
		sallary = 550000.95f;
		phno = 1258697825l;
		id = 0147;
		pinno = 456456;

		System.out.println(name);
		System.out.println(age);
		System.out.println(sallary);
		System.out.println(phno);
		System.out.println(id);
		System.out.println(pinno);
	}
}
